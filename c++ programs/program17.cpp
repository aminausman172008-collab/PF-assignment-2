#include <iostream>
using namespace std;

int main() {
    double vi = 10.0; 
    double t = 5.0;  
    double s;      

    s = vi * t;
    
    cout << "Initial velocity (vi): " << vi << " m/s" << endl;
    cout << "Time (t): " << t << " s" << endl;
    cout << "Displacement (s) = vi * t = " << s << " meters" << endl;

    return 0;
}

